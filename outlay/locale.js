export class Locale {
  static setUserLang() {
    // https://qna.habr.com/q/338809
    userLang = (navigator.language || navigator.userLanguage)
      .substr(0, 2)
      .toLowerCase();
    //userLang = "de";
    if (!Object.getOwnPropertyDescriptor(localeStringArray, userLang)) {
      userLang = "en";
    }
    localeString = localeStringArray[userLang];
  }
}
